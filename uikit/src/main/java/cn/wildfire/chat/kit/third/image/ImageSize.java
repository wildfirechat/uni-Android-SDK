package cn.wildfire.chat.kit.third.image;

/**
 * 图片宽高
 * @author dhl
 */
public class ImageSize {
    private int width;
    private int height;
    public int getWidth() {
        return width;
    }
    public void setWidth(int width) {
        this.width = width;
    }
    public int getHeight() {
        return height;
    }
    public void setHeight(int height) {
        this.height = height;
    }
}
